              <div class="post_commentbox"> <a href="http://www.how2test.com"><img src="http://www.how2test.com/images/h2t.jpg"></a> <span><i class="fa fa-calendar"></i>6:49 AM</span> <a href="#"><i class="fa fa-tags"></i>Selenium Tutorial</a> </div>
              <p><b>Firebug (Firefox Plug-in)</b> is a web development tool that helps in debugging, editing and monitoring of any website's CSS, HTML, DOM, XHR and JavaScript. 
			  <p>Firebug is used to inspect the UI elements of the web application under test.</p>
			  <p class="centerpara">How to Install Firebug plugin?</p>
              <p>1. Launch Firefox. If not installed, You can download and install firefox here. <a href="https://www.mozilla.org/en-US/firefox/new/">Download Firefox</a></p>
			  <img src="http://www.how2test.com/images/selenium/download_firefox.jpg" />
			  <p></p>
			  <p>2. Download Firebug <a href="https://getfirebug.com/downloads">here</a></p>
			  <img src="http://www.how2test.com/images/selenium/download_firebug.jpg" />
			  <p></p>
			  <p>3. Click <b>Add to Firefox</b> button as Highlighted. </p>
			  <img src="http://www.how2test.com/images/selenium/add_firebug.jpg" />
			  <p></p>
			  <p>4. Wait until download finishes & a pop up appears to install firebug. Click Install Now as shown below. </p>
			  <img src="http://www.how2test.com/images/selenium/install_firebug.jpg" />
			  <p></p>
			  <p>5. After installing firebug, restart firefox browser.</p>
			  <p>After restart, you can find firebug icon displayed on firefox browser as shown.</p>
			  <img src="http://www.how2test.com/images/selenium/firebug_on_firefox.jpg" />
			  <p></p>
			  <p>Firebug set up has been completed. You can start using it. Let's Learn how to use firebug in next article.</p>
